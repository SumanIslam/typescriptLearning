const add = (a: number, b:number): number => {
  return a + b;
}

const substract = (a: number, b:number): number => {
  return a - b;
}

// function declaration
function multiply(a: number, b: number): number {
  return a * b;
}

// anonymous function
const divide = function(a: number, b: number): number {
  return a / b;
}